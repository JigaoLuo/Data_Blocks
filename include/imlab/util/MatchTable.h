/**
 * Size of the MatchTable(pre-computed position table)
 * Idea from Paper
 */
constexpr uint16_t VECTOR8_INT32_ARRAY_SIZE = 256;

/**
 * Vector of 8 32-bit integers. 8 * 32 = 256
 * Representing a AVX256 Register with 256 bit length
 * Idea from the Paper
 */
typedef union {
    int32_t cell[8];
    __m256i reg256;
    __m128i reg128[2];
} vector8_int32;

/**
 * Function to build the fix MatchTable(pre-computed position table) in the Compile Time
 * Idea from the Paper
 */
constexpr auto vector8_int32_create_array() {
    std::array<vector8_int32, VECTOR8_INT32_ARRAY_SIZE> array {};
    for (size_t i = 0; i < VECTOR8_INT32_ARRAY_SIZE; ++i) {
        uint8_t bits = (uint8_t)i;

        /// Counting the set bits
        const size_t count = __builtin_popcount(bits);

        for (size_t index = 0, position_counter = 0; index < 8 && position_counter < count; ++index) {
            if (bits & 1) {
                array[i].cell[position_counter] = (index << 8) + count;
                ++position_counter;  // for writing the entry in, because the matched entry first come in
            }
            bits >>= 1;
        }
        for (size_t index = count; index < 8; ++index) {
            array[i].cell[index] = 0xFFFFFF00 + count;  // filled with FFFFFF + Num
        }
    }
    return array;
}

