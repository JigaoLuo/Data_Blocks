// ---------------------------------------------------------------------------------------------------
#include <assert.h>
#include <cstdlib>
#include "imlab/util/Random.h"
// ---------------------------------------------------------------------------------------------------
namespace imlab {
namespace util {
// ---------------------------------------------------------------------------------------------------
Random::Random(uint64_t seed) : seed(seed) {
  assert(seed != 0);
}
// ---------------------------------------------------------------------------------------------------
uint64_t Random::Rand() {
  seed ^= (seed << 13);
  seed ^= (seed >> 7);
  return (seed ^= (seed << 17));
}
// ---------------------------------------------------------------------------------------------------
float Random::RandScaleFactor() {
  return static_cast<float>(Rand()) / std::numeric_limits<uint64_t>::max();
}
// ---------------------------------------------------------------------------------------------------
}  // namespace util
}  // namespace imlab
